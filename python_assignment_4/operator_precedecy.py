#operator function
#exponentiation operator
print("2 to the power of 3 is:", 2 ** 3)

#multiplication operator
print("Multiplication of 4 and 5 is:", 4 * 5)

#exponentiation operator vs multiplication operator
print("2 to the power of 3 multiplied by 4 is:", 2 ** 3 * 4)

#addition operator
print("Addition of 2 and 3 is:", 2 + 3)

#addition operator vs multiplication operator
print("2 plus 3 multiplied by 4 is:", 2 + 3 * 4)

#subtraction operator
print("Subtraction of 5 and 2 is:", 5 - 2)

#subtraction operator vs multiplication operator
print("5 minus 2 multiplied by 3 is:", 5 - 2 * 3)